function createInterior(thePlayer, commandName, interiorId, inttype, cost, ...)
	if (exports.global:isPlayerLeadAdmin(thePlayer)) then
		if not (interiorId) or not (inttype) or not (cost) or not (...) or ((tonumber(inttype)<0) or (tonumber(inttype)>3)) then
			outputChatBox("SYNTAX: /" .. commandName .. " [Interior ID] [TYPE] [Cost] [Name]", thePlayer, 255, 194, 14)
			outputChatBox("TYPE 0: House", thePlayer, 255, 194, 14)
			outputChatBox("TYPE 1: Business", thePlayer, 255, 194, 14)
			outputChatBox("TYPE 2: Government (Unbuyable)", thePlayer, 255, 194, 14)
			outputChatBox("TYPE 3: Rentable", thePlayer, 255, 194, 14)
		else
			name = table.concat({...}, " ")
			
			local x, y, z = getElementPosition(thePlayer)
			local dimension = getElementDimension(thePlayer)
			local interiorwithin = getElementInterior(thePlayer)
			
			local inttype = tonumber(inttype)
			local owner = nil
			local locked = nil
			
			if (inttype==2) then
				owner = 0
				locked = 0
			else
				owner = -1
				locked = 1
			end
			
			interiorId = tonumber(interiorId)
			
			local ix = tonumber(interiors[interiorId][2])
			local iy = tonumber(interiors[interiorId][3])
			local iz = tonumber(interiors[interiorId][4])
			local optAngle = tonumber(interiors[interiorId][5])
			local interior = tonumber(interiors[interiorId][1])
			local max_items = tonumber(interiors[interiorId][6])
			
			local rot = getPedRotation(thePlayer)
			local query = mysql_query(handler, "INSERT INTO interiors SET x='" .. x .. "', y='" .. y .."', z='" .. z .."', type='" .. inttype .. "', owner='" .. owner .. "', locked='" .. locked .. "', cost='" .. cost .. "', name='" .. name .. "', interior='" .. interior .. "', interiorx='" .. ix .. "', interiory='" .. iy .. "', interiorz='" .. iz .. "', dimensionwithin='" .. dimension .. "', interiorwithin='" .. interiorwithin .. "', angle='" .. optAngle .. "', angleexit='" .. rot .. "', max_items='" .. max_items .. "'")
			
			if (query) then
				local id = mysql_insert_id(handler) -- Get the ID of the latest insert
				
				mysql_free_result(query)
				
				--=== SET ELEMENT DATA FOR PICKUPS ===----
				if (tonumber(inttype)==0) then -- House
					local pickup = createPickup(x, y, z, 3, 1273)
					local intpickup = createPickup(ix, iy, iz, 3, 1318)
					exports.pool:allocateElement(pickup)
					exports.pool:allocateElement(intpickup)
					
					-- set the default pickup data
					setPickupElementData(pickup, id, ix, iy, iz, optAngle, interior, locked, owner, inttype, cost, name,"0", "0", max_items, -1, 0,100,  interiorwithin, x, y, z, dimension, 0)
					setIntPickupElementData(intpickup, id, x, y, z, rot, locked, owner, inttype, interiorwithin, dimension, interior, ix, iy, iz)
				
				elseif (tonumber(inttype)==1) then -- Business
					local pickup = createPickup(x, y, z, 3, 1272)
					local intpickup = createPickup(ix, iy, iz, 3, 1318)
					exports.pool:allocateElement(pickup)
					exports.pool:allocateElement(intpickup)

					setPickupElementData(pickup, id, ix, iy, iz, optAngle, interior, locked, owner, inttype, cost, name,"0", "0", max_items, "", 0,100, interiorwithin, x, y, z, dimension, 0)
					setIntPickupElementData(intpickup, id, x, y, z, rot, locked, owner, inttype, interiorwithin, dimension, interior, ix, iy, iz)
				
				elseif (tonumber(inttype)==2) then -- Government / Other
					local pickup = createPickup(x, y, z, 3, 1318)
					local intpickup = createPickup(ix, iy, iz, 3, 1318)
					exports.pool:allocateElement(pickup)
					exports.pool:allocateElement(intpickup)
					
					setPickupElementData(pickup, id, ix, iy, iz, optAngle, interior, locked, owner, inttype, 0, name,"0", "0", 0, -1, 0,100, interiorwithin, x, y, z, dimension, 0)
					setIntPickupElementData(intpickup, id, x, y, z, rot, locked, owner, inttype, interiorwithin, dimension, interior, ix, iy, iz)
				elseif (tonumber(inttype)==3) then -- Rentable
					local pickup = createPickup(x, y, z, 3, 1273)
					local intpickup = createPickup(ix, iy, iz, 3, 1318)
					exports.pool:allocateElement(pickup)
					exports.pool:allocateElement(intpickup)
					
					setPickupElementData(pickup, id, ix, iy, iz, optAngle, interior, locked, owner, inttype, 0, name,"0", "0", 0, -1, 0,100, interiorwithin, x, y, z, dimension, 0)
					setIntPickupElementData(intpickup, id, x, y, z, rot, locked, owner, inttype, interiorwithin, dimension, interior, ix, iy, iz)
				end
			else
				outputChatBox("Failed to create interior - Invalid characters used in name of the interior.", thePlayer, 255, 0, 0)
			end
		end
	end
